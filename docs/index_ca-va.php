        <a name="makeOrder"></a>
        <h2>Com faig la meva comanda?</h2>
        <ol>
            <li>Fer clic a "Propera Comanda".</li>
            <li>Seleccionar "Per proveïdor" o "Per categoria de productes",
                apareixeran els productes habilitats per a la data indicada.</li>
            <li>Completar les quantitats de cada producte i en fer clic fora o tabulador,
                la quantitat queda ingressada.</li>
            <li>Finalment en el panell de resum (a la dreta), fer clic a "Desa".</li>
        </ol>
    </div>

    <div class = "faq-item">
        <a name="printOrder"></a>
        <h2>Com imprimeixo la meva comanda?</h2>
        <p>Per obtenir una versió imprimible de la comanda, n'hi ha prou de fer el següent:</p>
        <ol>
            <li>A "Inici" fer clic a "Les meves comandes"</li>
            <li>A la part superior dreta de les comandes, on acaba la línia del títol,
                apareixerà una icona d'impressora.<br>
                En fer clic, obtindràs una versió imprimible de la teva comanda.</li>
        </ol>
    </div>

    <div class = "faq-item">
        <a name="seeMonney"></a>
        <h2>Com veig el meu saldo de diners?</h2>
        <p>Per veure el saldo i els moviment de la teva UF, pots fer servir l'opció "Diners".</p>
        <ol>
            <li>A la dreta de la línia de menús fes clic a "El meu compte".</li>
            <li>S'obrirà un sub-menú, llavors fes clic a la l'opció "Diners",
                allà veuràs els moviment de diners de la teva UF i el saldo.</li>
        <ol>
    </div>


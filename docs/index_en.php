        <h2>How to make my order?</h2>
        <ol>
            <li>Click "Place order".</li>
            <li>Select "By provider" or "by product" enabled products
                by the date indicated will appear.</li>
            <li>Complete the quantities of each product and click out or tab, the amount is entered.</li>
            <li>Finally in the summary panel (right), click "Save".</li>
        <ol>
    </div>

    <div class = "faq-item">
        <a name="printOrder"></a>
        <h2>How to print my order?</h2>
        <p>For a printable version of the order, simply do the following:</p>
        <ol>
            <li>In "Home" click on "My orders"</li>
            <li>At the top right of the orders,
                where it ends the title line, a printer icon appears.<br>
                When you click it, you'll get a printable version of your order.</li>
        </ol>
    </div>

    <div class = "faq-item">
        <a name="seeMonney"></a>
        <h2>How do I see my balance money?</h2>
        <p>To see the balance and movement of your HU, you can use the "Money" option.</ P>
        <ol>
            <li>On the right of the line menu click on "My Account".</li>
            <li>A sub-menu appears, then click on "Money" option,
                there you will see the movements of money from your HU and balance.</li>
        </ol>

    <div class="faq-item">
        <a name="makeOrder"></a>
        <h2>¿Cómo hago mi pedido?</h2>
        <ol>
            <li>Hacer clic en "Siguiente pedido".</li>
            <li>Seleccionar "Por proveedor" o "Por categoría de productos", 
                aparecerán los productos habilitados para la fecha indicada.</li>
            <li>Completar las cantidades de cada producto y al hacer clic fuera o tabulador,
                la cantidad queda ingresada.</li>
            <li>Finalmente en el panel de resumen (a la derecha), hacer clic en "Guardar".</li>
        <ol>
    </div>

    <div class="faq-item">
        <a name="printOrder"></a>
        <h2>¿Cómo imprimo mi pedido?</h2>
        <p>Para obtener una versión imprimible del pedido, basta hacer lo siguiente:</p>
        <ol>
            <li>En "Inicio" hacer clic en "Mis pedidos"</li>
            <li>En la parte superior derecha de los pedidos,
                donde termina la línea del título, aparecerá un icono de impresora.<br>
                Al hacer clic en él, obtendrás una versión imprimible de tu pedido.</li>
        <ol>
    </div>


    <div class="faq-item">
        <a name="seeMonney"></a>
        <h2>¿Cómo veo mi saldo de dinero?</h2>
        <p>Para ver el saldo y los movimiento de tu UF, puedes usar la opción "Dinero".</p>
        <ol>
            <li>En la derecha de la línea de menús haz clic en "Mi cuenta".</li>
            <li>Se abrirá un sub-menú, entonces haz clic en la opción "Dinero",
                allí verás los movimiento de dinero de tu UF y el saldo.</li>
        <ol>
    </div>

